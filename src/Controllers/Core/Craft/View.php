	/**
	 * Compile view data efficiently
	 *
	 * Optimizes data compilation by:
	 * - Avoiding unnecessary array merges
	 * - Reusing compiled data when possible
	 * - Minimizing memory allocations
	 *
	 * @param array $formElements Form elements data
	 * @param array $tableElements Table elements data
	 * @param array $chartElements Chart elements data
	 * @param mixed $data Additional data to merge
	 * @return array Compiled content page data
	 *
	 * @performance Reduces array operations and memory usage during data compilation
	 */
	private function compileContentData(array $formElements, array $tableElements, array $chartElements, mixed $data = false): array {
		// Start with base content page data
		$compiledData = $this->data['content_page'];

		// If no additional data, just merge elements
		if (false === $data) {
			// Merge all elements at once instead of multiple merges
			return array_merge($compiledData, $formElements, $tableElements, $chartElements);
		}

		// Handle additional data
		if (!is_array($data)) {
			// Single value - wrap in array
			$dataContents = [$data];
		} else {
			// Array data
			if (canvastack_is_empty($data)) {
				// Empty array - no need to merge
				return array_merge($compiledData, $formElements, $tableElements, $chartElements);
			}
			$dataContents = $data;
		}

		// Merge all data at once (single operation instead of multiple)
		return array_merge($compiledData, $dataContents, $formElements, $tableElements, $chartElements);
	}
	
