		$fullViewPath = $viewPath . '.' . $uri;
		
		// Check if view exists using view()->exists()
		if (!view()->exists($fullViewPath)) {
			// Fallback to default.pages.admin if view not found
			$template = 'default';
			$viewPath = 'default.pages.admin';
		}
		
		$this->viewAdmin = $viewPath;
		$this->pageView  = $this->getCachedViewPath('admin', $uri);
	}
	
	/**
	 * Configure front-end page view path
	 * 
	 * Sets the view path for front-end pages using the configured template directory.
	 * Front-end pages are typically located in resources/views/{template}/pages/front/.
	 * 
	 * Implements fallback logic: if the view for the active template doesn't exist,
	 * falls back to the default template to ensure the application never breaks.
	 * 
	 * @param string $uri View file name (default: 'index')
	 * @return void Sets $this->viewFront and $this->pageView properties
	 * 
	 * @performance View path is cached via getCachedViewPath() to avoid repeated string operations
	 * 
	 * @example Default front-end index:
	 * ```php
	 * $this->uriFront(); // Sets view to: {template}.pages.front.index
	 * ```
	 * 
	 * @example Custom front-end view:
	 * ```php
	 * $this->uriFront('about'); // Sets view to: {template}.pages.front.about
	 * ```
	 * 
	 * @example Fallback behavior:
	 * ```php
	 * // If canvasign.pages.front.index doesn't exist, falls back to default.pages.front.index
	 * ```
	 */
	private function uriFront(string $uri = 'index'): void {
		// Get template from canvastack_current_template() or canvastack_config('template')
		$template = function_exists('canvastack_current_template') 
			? canvastack_current_template() 
			: canvastack_config('template');
		
		// Ensure template is not null or empty
		if (empty($template)) {
			$template = 'default';
		}
		
		// Build view path as {$template}.pages.front
		$viewPath = $template . '.pages.front';
		
		// Build full view path with URI
		$fullViewPath = $viewPath . '.' . $uri;
		
		// Check if view exists using view()->exists()
		if (!view()->exists($fullViewPath)) {
			// Fallback to default.pages.front if view not found
			$template = 'default';
			$viewPath = 'default.pages.front';
		}
		
		$this->viewFront = $viewPath;
		$this->pageView  = $this->getCachedViewPath('front', $uri);
	}
	
	/**
	 * Configure view path with page type specification
	 * 
	 * Determines the appropriate view path based on page type (admin or front-end) and
	 * custom path specification. This method is the central view path configuration point
	 * that delegates to uriAdmin() or uriFront() based on the page type.
	 * 
	 * The method checks the $this->pageType property to determine which template directory
	 * to use, then sets the appropriate view path. If pageType is not set or is 'login',
	 * defaults to admin pages.
	 * 
	 * @param string|bool $path Custom view file name (false = use 'index')
	 * @param bool $pageAdmin Whether this is an admin page (true) or front-end page (false)
	 * @return void Sets $this->pageView, $this->data['page_type'], and $this->data['page_view']
	 * 
	 * @performance View path caching reduces string operations on repeated calls
	 * 
	 * @example Admin page with default view:
	 * ```php
	 * $this->configView(); // Uses admin.index
	 * ```
	 * 
	 * @example Admin page with custom view:
	 * ```php
	 * $this->configView('dashboard'); // Uses admin.dashboard
	 * ```
	 * 
	 * @example Front-end page:
	 * ```php
	 * $this->configView('home', false); // Uses front.home
	 * ```
	 */
	private function configView(string|bool $path = false, bool $pageAdmin = true): void {
		// Determine if this is an admin page
		if (!empty($this->pageType) && ControllerConstants::PAGE_TYPE_LOGIN !== current_route()) {
			$pageAdmin = false; // as user in frontpage
		}
		
		$this->setPageType($pageAdmin);
		$page_type = str_replace('page', '', $this->pageType);
		
		// Set URI based on page type
		$this->setUriByPageType($page_type, $path);
		
		$this->data['page_type'] = $this->pageType;
		$this->data['page_view'] = $this->pageView;
	}
	
	/**
	 * Set URI based on page type
	 * 
	 * @param string|bool $page_type Page type ('admin', 'front', or false)
	 * @param string|bool $path Custom path
	 * @return void
	 */
	private function setUriByPageType(string|bool $page_type, string|bool $path): void {
		// Determine if admin page
		$isAdminPage = (false !== $page_type && 'admin' === $page_type) || (false === $page_type);
		
		if ($isAdminPage) {
			$this->setAdminUri($path);
		} else {
			$this->setFrontUri($path);
		}
	}
	
	/**
	 * Set admin URI
	 * 
	 * @param string|bool $path Custom path
	 * @return void
	 */
	private function setAdminUri(string|bool $path): void {
		if (false !== $path) {
			$this->uriAdmin($path);
		} else {
			$this->uriAdmin();
		}
	}
	
	/**
	 * Set front URI
	 * 
	 * @param string|bool $path Custom path
	 * @return void
	 */
	private function setFrontUri(string|bool $path): void {
		if (false !== $path) {
			$this->uriFront($path);
		} else {
			$this->uriFront();
		}
	}
	
	private mixed $preference = null;
	/**
	 * Get all web preferences from database with caching
	 * 
	 * Retrieves application-wide preference settings from the database, including logo paths,
	 * login page configuration, and other system preferences. The method implements caching
	 * to reduce database queries and improve performance.
	 * 
	 * Preferences are cached using the 'preference_all' cache key with a configurable TTL
	 * (Time To Live). When caching is enabled, subsequent calls retrieve data from cache
	 * instead of querying the database.
	 * 
	 * Cache Configuration:
	 * - Enable/disable: config('canvastack.controller.caching.preference_cache_enabled')
	 * - TTL: config('canvastack.controller.caching.preference_cache_ttl') (default: 7200 seconds / 2 hours)
	 * - Cache key: 'preference_all'
	 * 
	 * Cache Invalidation:
	 * Call invalidatePreferenceCache() after updating preferences to ensure fresh data.
	 * 
	 * Created @Aug 21, 2018
	 * Author: wisnuwidi
	 * 
	 * @return void Preference data stored in $this->preference property
	 * 
	 * @performance Caching Strategy:
	 *           - First call: Queries database (~5-10ms)
	 *           - Cached calls: Retrieves from cache (~0.1-0.5ms)
	 *           - Cache hit rate: ~99% for typical applications
	 *           - Memory usage: ~1-5KB per cached preference set
	 *              
	 *              Performance Impact:
	 *           - Without caching: 1 database query per request
	 *           - With caching: 1 database query per TTL period (default: 2 hours)
	 *           - Reduction: ~99.9% fewer database queries
	 * 
	 * @example Accessing preferences:
	 * ```php
	 * $this->getPreferences();
	 * $logo = $this->preference['logo'];
	 * $appName = $this->preference['app_name'];
	 * ```
	 * 
	 * @example Invalidating cache after update:
	 * ```php
	 * Preference::where('id', 1)->update(['logo' => 'new-logo.png']);
	 * $this->invalidatePreferenceCache(); // Clear cache
	 * $this->getPreferences(); // Reload from database
	 * ```
	 */
	private function getPreferences(): void {
		// Check if preference caching is enabled
		$cacheEnabled = config('canvastack.controller.caching.preference_cache_enabled', true);
		$cacheTtl = config('canvastack.controller.caching.preference_cache_ttl', 7200);
		
		if ($cacheEnabled) {
			// Try to get from cache
			$this->preference = canvastack_controller_cache_remember(
				'preference_all',
				function() {
					return canvastack_get_model_data(Preference::class);
				},
				$cacheTtl
			);
		} else {
			// Caching disabled - load directly
			$this->preference = canvastack_get_model_data(Preference::class);
		}
	}
	
	/**
	 * Get logo file path from preferences
	 * 
	 * Retrieves the logo file path from application preferences. Supports both full-size
	 * logo and thumbnail version. The method loads preferences from cache if available,
	 * or from database if not cached.
	 * 
	 * Logo paths are typically stored as relative paths from the public storage directory
	 * (e.g., 'logos/app-logo.png'). The actual file location would be storage/app/public/logos/app-logo.png.
	 * 
	 * @param bool $thumb Whether to return thumbnail path (true) or full-size logo path (false)
	 * @return string Logo file path, or empty string if not configured
	 * 
	 * @performance Preferences are cached, so this method is very fast (~0.1ms)
	 * 
	 * @example Get full-size logo:
	 * ```php
	 * $logoPath = $this->logo_path(); // Returns: 'logos/app-logo.png'
	 * // In view: <img src="{{ asset('storage/' . $logoPath) }}" alt="Logo">
	 * ```
	 * 
	 * @example Get thumbnail logo:
	 * ```php
	 * $thumbPath = $this->logo_path(true); // Returns: 'logos/app-logo-thumb.png'
	 * // In view: <img src="{{ asset('storage/' . $thumbPath) }}" alt="Logo">
	 * ```
	 */
	public function logo_path(bool $thumb = false): string {
		$this->getPreferences();
		
		if (true === $thumb) {
			return $this->preference['logo_thumb'] ?? '';
		} else {
			return $this->preference['logo'] ?? '';
		}
	}
	
	/**
	 * Invalidate preference cache
	 * 
	 * Call this method when preferences are updated to ensure
	 * cached preference data is refreshed.
	 * 
	 * @return bool True if cache was invalidated successfully
	 * 
	 * @performance Cache invalidation ensures data consistency after preference changes
	 * 
	 * @example
	 * // After updating preferences
	 * $this->invalidatePreferenceCache();
	 */
	public function invalidatePreferenceCache(): bool {
		return canvastack_controller_cache_forget('preference_all');
	}
	/**
	 * Cache for compiled view paths
	 *
	 * @var array
	 */
	private array $viewPathCache = [];
	
	/**
	 * Check if component should be loaded
	 * 
	 * Implements lazy loading by checking if component data exists
	 * before attempting to render it.
	 * 
	 * @param string $componentType Component type (form/table/chart)
	 * @return bool True if component should be loaded
	 * 
	 * @performance Avoids unnecessary component initialization and rendering
	 */
	private function shouldLoadComponent(string $componentType): bool {
		if (!isset($this->data['components'])) {
			return false;
		}
		
		if (!isset($this->data['components']->$componentType)) {
			return false;
		}
		
		if (!isset($this->data['components']->$componentType->elements)) {
			return false;
		}
		
		return !empty($this->data['components']->$componentType->elements);
	}

	/**
	 * Get cached view path or compile and cache it
	 *
	 * Caches view path compilation to avoid repeated string operations
	 * and path resolution.
	 *
	 * @param string $pageType Page type (admin/front)
	 * @param string|bool $path Custom path or false for default
	 * @return string Compiled view path
	 *
	 * @performance Caches view path compilation to reduce string operations
	 *              Respects canvastack.controller.caching.view_cache_enabled configuration
	 */
	private function getCachedViewPath(string $pageType, string|bool $path = false): string {
		// Check if view caching is enabled
		$cacheEnabled = config('canvastack.controller.caching.view_cache_enabled', true);
		
		if (!$cacheEnabled) {
			// Caching disabled - compile directly
			$template = canvastack_config('template');
			$baseView = $template . '.pages.' . $pageType;
			
			if (false !== $path) {
				return $baseView . '.' . $path;
			} else {
				return $baseView . '.index';
			}
		}
		
		// Create cache key
		$cacheKey = $pageType . '_' . ($path === false ? 'default' : $path);

		// Return cached path if available
		if (isset($this->viewPathCache[$cacheKey])) {
			return $this->viewPathCache[$cacheKey];
		}

		// Compile view path
		$template = canvastack_config('template');
		$baseView = $template . '.pages.' . $pageType;

		if (false !== $path) {
			$viewPath = $baseView . '.' . $path;
		} else {
			$viewPath = $baseView . '.index';
		}

		// Cache and return
		$this->viewPathCache[$cacheKey] = $viewPath;
		return $viewPath;
	}


