	 * Check and remove disabled action buttons
	 * 
	 * Processes the removeButtons array to filter out action buttons that should not be
	 * displayed for the current page. This method supports removing common action buttons
	 * like 'add', 'view', 'delete', and 'back' based on controller configuration.
	 * 
	 * The method searches for button labels containing the specified action keywords and
	 * removes them from the route_info->action_page array, preventing them from being
	 * rendered in the view.
	 * 
	 * @return void Modifies $this->data['route_info']->action_page in place
	 * 
	 * @example Removing buttons in controller:
	 * ```php
	 * class UserController extends Controller {
	 *     protected $removeButtons = ['delete', 'back'];
	 *     
	 *     public function show($id) {
	 *         // Delete and back buttons automatically removed
	 *         return $this->render();
	 *     }
	 * }
	 * ```
	 */
	private function checkIfAnyButtonRemoved(): void {
		if (!empty($this->removeButtons)) {
			$add    = null;
			$view   = null;
			$delete = null;
			$back   = null;
			
			foreach ($this->removeButtons as $action) {
				if (canvastack_string_contained($action, 'add'))    $add    = $action;
				if (canvastack_string_contained($action, 'view'))   $view   = $action;
				if (canvastack_string_contained($action, 'delete')) $delete = $action;
				if (canvastack_string_contained($action, 'back'))   $back   = $action;
			}
			
			if (!empty($this->data['route_info'])) {
    			foreach (array_keys($this->data['route_info']->action_page) as $key) {
    				if (!empty($add)    && canvastack_string_contained($key, $add))    unset($this->data['route_info']->action_page[$key]);
    				if (!empty($view)   && canvastack_string_contained($key, $view))   unset($this->data['route_info']->action_page[$key]);
    				if (!empty($delete) && canvastack_string_contained($key, $delete)) unset($this->data['route_info']->action_page[$key]);
    				if (!empty($back)   && canvastack_string_contained($key, $back))   unset($this->data['route_info']->action_page[$key]);
    			}
			}
		}
	}
	
