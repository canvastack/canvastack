	 */
	protected function setPage(?string $page = null, string|bool $path = false): void {
		$this->page_name = $page;
		$this->set_session();
		if (!empty($this->session['user_group'])) $this->is_root = str_contains($this->session['user_group'], 'root');
		$this->routeInfo();
		
		if (!empty($this->session['id'])) $this->filter_page = canvastack_mapping_page(intval($this->session['id']));
		if (!empty($this->model_class))   $this->model($this->model_class);
		if (is_empty($page)) {
			$currentPage = last(explode('.', current_route()));
			if (str_contains(strtolower($currentPage), 'index')) {
				$currentModule = strtolower(str_replace('Controller', '', class_basename($this))) . ' Lists';
			} else {
				$currentModule = $currentPage . ' ' . strtolower(str_replace('Controller', '', class_basename($this)));
			}
		} else {
			$currentModule = $page;
		}
		
		$page_name  = canvastack_underscore_to_camelcase($currentModule);
		$page_title = strtolower($currentModule);
		
		// Escape page name and title to prevent XSS
		$safePage_name  = $this->escapePageTitle($page_name);
		$safePage_title = $this->escapePageTitle($page_title);
		
		$this->meta->title($safePage_name);
		$this->template->set_breadcrumb (
			$safePage_name,  [$safePage_title => url($this->template->currentURL), 'index'],
			$safePage_title, [$safePage_title, 'home']
		);
		$this->configView($path);
		$this->filterPage();
		canvastack_log_activity();
	}
	
