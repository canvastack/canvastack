	/**
	 * Set Upload Path
	 * 
	 * @param string $folder_name Folder name for uploads
	 * 
	 * @return string Full upload path
	 * 
	 * @security Path is validated for traversal attempts
	 */
	
		/**
		 * Set Upload Path with Security Validation
		 * 
		 * PATH GENERATION LOGIC:
		 * ----------------------
		 * This method generates the full filesystem path where uploaded files will be stored.
		 * 
		 * Path Structure:
		 * - Base: public_path() + base_resources config (e.g., /var/www/public/assets)
		 * - Folder: User-specified folder name (e.g., uploads/avatars)
		 * - Result: /var/www/public/assets/uploads/avatars
		 * 
		 * Security Measures:
		 * 1. Validates path against base directory to prevent traversal
		 * 2. Uses realpath() to resolve symbolic links
		 * 3. Ensures path stays within public/assets directory
		 * 4. Logs security violations
		 * 
		 * Example Flow:
		 * - Input: "uploads/avatars"
		 * - Base: "/var/www/public/assets"
		 * - Output: "/var/www/public/assets/uploads/avatars"
		 * 
		 * @param string $folder_name Folder name relative to base_resources
		 * 
		 * @return string Full filesystem path for uploads
		 * 
		 * @throws \InvalidArgumentException If path validation fails
		 * 
		 * @security CRITICAL - Prevents directory traversal attacks
		 */
		private function setUploadPath(string $folder_name): string {
			$baseFileUpload = canvastack_config('base_resources');
			$uploadPath = public_path("{$baseFileUpload}/{$folder_name}");

			// Security: Validate path
			$basePath = public_path($baseFileUpload);
			try {
				$this->validateUploadPath($uploadPath, $basePath);
			} catch (\InvalidArgumentException $e) {
				error_log('SECURITY ERROR: Invalid upload path: ' . $e->getMessage());
				throw $e;
			}

			return $uploadPath;
		}

	
	/**
	 * Set Asset Path
	 * 
	 * @param string $path Full file path
	 * @param string $folder Folder name
	 * 
	 * @return string Public URL path
	 */
	
		/**
		 * Set Asset Path for HTTP Access
		 * 
		 * Generates a public URL path that is HTTP accessible for uploaded files.
		 * This method ensures the path is properly formatted for web access.
		 * 
		 * @param string $path Full file path
		 * @param string $folder Folder name or filename
		 * 
		 * @return string Public URL path (e.g., "assets/uploads/2024/01/15/file.jpg")
		 * 
		 * @security Ensures path is within public directory
		 */
		
			/**
			 * Set Asset Path for HTTP Access
			 * 
			 * PATH GENERATION LOGIC:
			 * ----------------------
			 * This method converts a filesystem path to an HTTP-accessible URL path.
			 * 
			 * Conversion Process:
			 * 1. Extract path relative to public directory
			 * 2. Remove system-specific separators (backslashes)
			 * 3. Handle different base URL configurations
			 * 4. Normalize path for web access
			 * 
			 * Path Transformation Examples:
			 * 
			 * Example 1 (Standard):
			 * - Input: "/var/www/public/assets/uploads/2024/01/15", "file.jpg"
			 * - Output: "assets/uploads/2024/01/15/file.jpg"
			 * 
			 * Example 2 (With index folder):
			 * - Input: "/var/www/public/index/assets/uploads/2024/01/15", "file.jpg"
			 * - Config: index_folder = "index/"
			 * - Output: "index/assets/uploads/2024/01/15/file.jpg"
			 * 
			 * Example 3 (Thumbnail):
			 * - Input: "/var/www/public/assets/uploads/2024/01/15/thumb", "tnail_file.jpg"
			 * - Output: "assets/uploads/2024/01/15/thumb/tnail_file.jpg"
			 * 
			 * Round-Trip Property:
			 * - Upload: File saved to filesystem path
			 * - Generate: Asset path created for HTTP access
			 * - Retrieve: Asset path returned to application
			 * - Display: Asset path used in HTML (img src, a href)
			 * - Access: Browser requests file via HTTP using asset path
			 * 
			 * The round-trip property ensures:
			 * ∀ file uploaded, asset_path = setAssetPath(upload_path, filename)
			 * ⇒ HTTP GET asset_path returns the uploaded file
			 * 
			 * @param string $path Full filesystem path
			 * @param string $folder Folder name or filename
			 * 
			 * @return string Public URL path (e.g., "assets/uploads/2024/01/15/file.jpg")
			 * 
			 * @security Ensures path is within public directory
			 */
			private function setAssetPath(string $path, string $folder): string {
				$baseIndexFolder = canvastack_config('index_folder');
				$basePathURL     = explode($baseIndexFolder, str_replace('\\', '/', $path));

				// Handle different base URL configurations
				if (false === canvastack_string_contained(canvastack_config('baseURL'), 'public')) {
					$baseIndexFolder = null;
					if (!empty($basePathURL[2])) {
						$basePathURL[1]  = str_replace('/assets/', 'assets/', $basePathURL[2]);
					} else {
						$basePathURLi    = explode('/', $basePathURL[1]);
						unset($basePathURLi[0]);
						$basePathURL[1]  = implode('/', $basePathURLi);
					}
				}

				$endBasePathURL = end($basePathURL);

				// Construct the final asset path
				$assetPath = "{$baseIndexFolder}{$endBasePathURL}/{$folder}";

				// Normalize path separators for web access
				$assetPath = str_replace('\\', '/', $assetPath);

				// Remove duplicate slashes
				$assetPath = preg_replace('#/+#', '/', $assetPath);

				// Ensure path doesn't start with slash (relative path for web)
				$assetPath = ltrim($assetPath, '/');

				return $assetPath;
			}


	
