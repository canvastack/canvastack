/**
 * File Upload Trait with Comprehensive Security
 * 
 * This trait provides secure file upload functionality with multiple layers of protection:
 * - File extension whitelist validation
 * - MIME type content validation (not just extension)
 * - Path traversal prevention
 * - Random filename generation
 * - Proper file permissions (0644)
 * - Thumbnail validation
 * - Comprehensive error handling and cleanup
 * - Security logging for all operations
 * 
 * PATH GENERATION ARCHITECTURE:
 * =============================
 * 
 * The file upload system uses a two-path architecture:
 * 
 * 1. FILESYSTEM PATH (Upload Path):
 *    - Generated by: setUploadPath()
 *    - Purpose: Where files are physically stored on disk
 *    - Format: /var/www/public/assets/uploads/2024/01/15
 *    - Security: Validated against base directory to prevent traversal
 * 
 * 2. HTTP PATH (Asset Path):
 *    - Generated by: setAssetPath()
 *    - Purpose: URL path for HTTP access in browser
 *    - Format: assets/uploads/2024/01/15/file.jpg
 *    - Security: Normalized for web access, no backslashes or traversal
 * 
 * ROUND-TRIP PROPERTY:
 * ====================
 * 
 * The round-trip property ensures uploaded files can be reliably retrieved and displayed:
 * 
 * Property Definition:
 * ∀ file F uploaded via fileUpload(path, request, info):
 *   1. F is saved to filesystem_path = setUploadPath(path)
 *   2. asset_path = setAssetPath(filesystem_path, filename)
 *   3. asset_path is stored in getFileUploads[inputname]['file']
 *   4. HTTP GET asset_path returns file F
 *   5. If F is image, thumbnail_path is also generated and accessible
 * 
 * Verification Methods:
 * - getUploadedFilePaths(): Retrieve stored asset paths
 * - verifyAssetPathAccessible(): Verify path is HTTP accessible
 * - verifyThumbnailPath(): Verify thumbnail path is correct
 * - testFileUploadRoundTrip(): Test complete upload → retrieve → display flow
 * 
 * Example Flow:
 * 1. Upload: POST /upload with file "avatar.jpg"
 * 2. Save: File saved to /var/www/public/assets/uploads/2024/01/15/abc123.jpg
 * 3. Generate: Asset path = "assets/uploads/2024/01/15/abc123.jpg"
 * 4. Store: getFileUploads['avatar']['file'] = asset path
 * 5. Retrieve: $form->getUploadedFilePaths('avatar')
 * 6. Display: <img src="assets/uploads/2024/01/15/abc123.jpg">
 * 7. Access: Browser GET /assets/uploads/2024/01/15/abc123.jpg → Success
 * 
 * THUMBNAIL GENERATION:
 * =====================
 * 
 * For image uploads, thumbnails are automatically generated:
 * - Thumbnail path: {upload_path}/thumb/tnail_{filename}
 * - Asset path: assets/uploads/2024/01/15/thumb/tnail_abc123.jpg
 * - Stored in: getFileUploads[inputname]['thumbnail']
 * - Verified by: verifyThumbnailPath()
 * 
 * @security CRITICAL - All file uploads go through multiple validation layers
 * @security File extensions are validated against whitelist
 * @security MIME types are validated from actual file content
 * @security Paths are validated to prevent directory traversal
 * @security Filenames are randomized to prevent overwrite attacks
 * @security File permissions are set to 0644 (read-only for group/others)
 * 
