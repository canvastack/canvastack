	/**
	 * Generate and save thumbnail
	 * 
	 * @param object $request Request object
	 * @param string $inputname Input field name
	 * @param array $thumbnailSize Thumbnail dimensions [width, height]
	 * @param string $thumbPath Thumbnail directory path
	 * @param string $thumbname Thumbnail filename
	 * 
	 * @return void
	 */
	private function generateAndSaveThumbnail(object $request, string $inputname, array $thumbnailSize, string $thumbPath, string $thumbname): void {
		$file = $request->file($inputname);
		
		// Initialize ImageManager with GD driver for Intervention Image v3
		$manager = new ImageManager(new Driver());
		$thumbfile = $manager->read($file->getRealPath());
		
		// Scale thumbnail based on dimensions
		$this->scaleThumbnail($thumbfile, $thumbnailSize);
		
		// Save thumbnail
		$fullThumbPath = "{$thumbPath}/{$thumbname}";
		$thumbfile->save($fullThumbPath);
		
		// Security: Set secure file permissions on thumbnail
		$this->setSecureFilePermissions($fullThumbPath);
	}

	/**
	 * Scale thumbnail to specified dimensions
	 * 
	 * @param object $thumbfile Thumbnail image object
	 * @param array $thumbnailSize Thumbnail dimensions [width, height]
	 * 
	 * @return void
	 */
	private function scaleThumbnail(object $thumbfile, array $thumbnailSize): void {
		$width = $thumbnailSize[0];
		$height = $thumbnailSize[1];
		
		// Both dimensions specified
		if ($width !== null && $height !== null) {
			$thumbfile->scale($width, $height);
			return;
		}
		
		// Only width specified
		if ($width !== null) {
			$thumbfile->scaleDown(width: $width);
			return;
		}
		
		// Only height specified
		if ($height !== null) {
			$thumbfile->scaleDown(height: $height);
		}
	}
	
	/**
	 * Get ARIA attributes for file input accessibility
	 * 
	 * Generates appropriate ARIA attributes based on field validation state and requirements.
	 * 
	 * Note: This method uses helper methods from AriaHelper trait.
	 * 
	 * @param string $name Field name
	 * @param array $attributes HTML attributes
	 * 
	 * @return array ARIA attributes
	 */
	private function getFileAriaAttributes(string $name, array $attributes): array {
		$ariaAttrs = [];
		
		// Check if field is required (uses AriaHelper trait's isFieldRequired method)
		$isRequired = $this->isFieldRequired($name, $attributes);
		if ($isRequired) {
			$ariaAttrs[FormConstants::ARIA_REQUIRED] = 'true';
		}
		
		// Check if field has validation errors (uses AriaHelper trait's hasValidationErrors method)
		$hasErrors = $this->hasValidationErrors($name);
		if ($hasErrors) {
			$ariaAttrs[FormConstants::ARIA_INVALID] = 'true';
			$ariaAttrs[FormConstants::ARIA_DESCRIBEDBY] = canvastack_form_escape_html($name) . '-error';
		}
		
		// Add aria-label if no visible label (uses AriaHelper trait's generateFieldLabel method)
		if (!isset($attributes['label']) || $attributes['label'] === false) {
			$labelText = $this->generateFieldLabel($name);
			$ariaAttrs[FormConstants::ARIA_LABEL] = $labelText;
			
			// Add file requirements to aria-label if present
			$requirements = $this->getFileRequirements($attributes);
			if (!empty($requirements)) {
				$ariaAttrs[FormConstants::ARIA_LABEL] .= '. ' . $requirements;
				$ariaAttrs[FormConstants::ARIA_DESCRIBEDBY] = canvastack_form_escape_html($name) . '-requirements';
			}
		} else {
			// Add aria-describedby for file requirements
			$requirements = $this->getFileRequirements($attributes);
			if (!empty($requirements)) {
				$ariaAttrs[FormConstants::ARIA_DESCRIBEDBY] = canvastack_form_escape_html($name) . '-requirements';
			}
		}
		
		return $ariaAttrs;
	}
	
	/**
	 * Get file requirements description for accessibility
	 * 
	 * @param array $attributes HTML attributes
	 * 
	 * @return string Requirements description
	 */
	private function getFileRequirements(array $attributes): string {
		$requirements = [];
		
		// Get allowed extensions
		if (!empty($attributes['allowed_extensions'])) {
			$extensions = is_array($attributes['allowed_extensions']) 
				? implode(', ', $attributes['allowed_extensions'])
				: $attributes['allowed_extensions'];
			$requirements[] = 'Allowed file types: ' . canvastack_form_escape_html($extensions);
		} else if (!empty($this->allowedFileExtensions)) {
			$extensions = implode(', ', array_slice($this->allowedFileExtensions, 0, 5));
			if (count($this->allowedFileExtensions) > 5) {
				$extensions .= ' and more';
			}
			$requirements[] = 'Allowed file types: ' . canvastack_form_escape_html($extensions);
		}
		
		// Get max file size
		if (!empty($attributes['max_file_size'])) {
			$maxSizeMB = round($attributes['max_file_size'] / 1048576, 2);
			$requirements[] = 'Maximum file size: ' . $maxSizeMB . 'MB';
		} else if (!empty($this->maxFileSize)) {
			$maxSizeMB = round($this->maxFileSize / 1048576, 2);
			$requirements[] = 'Maximum file size: ' . $maxSizeMB . 'MB';
		}
		
		return implode('. ', $requirements);
	}
	
	/**
	 * Render requirements description HTML with modern elegant design
	 * 
	 * @param string $nameEscaped Escaped field name
	 * @param array $ariaAttributes ARIA attributes
	 * 
	 * @return string Requirements HTML
	 */
	private function renderRequirementsDescription(string $nameEscaped, array $ariaAttributes): string {
		// Check if aria-describedby is set for requirements
		if (!isset($ariaAttributes[FormConstants::ARIA_DESCRIBEDBY])) {
			return '';
		}
		
		$describedById = $ariaAttributes[FormConstants::ARIA_DESCRIBEDBY];
		
		// Only render if it's for requirements (not errors)
		if (!str_contains($describedById, '-requirements')) {
			return '';
		}
		
		// Get requirements text from aria-label if present
		$requirementsText = '';
		if (isset($ariaAttributes[FormConstants::ARIA_LABEL])) {
			$parts = explode('. ', $ariaAttributes[FormConstants::ARIA_LABEL]);
			if (count($parts) > 1) {
				array_shift($parts); // Remove field name
				$requirementsText = implode('. ', $parts);
			}
		}
		
		if (empty($requirementsText)) {
			return '';
		}
		
		// Parse requirements into structured data
		$requirements = $this->parseRequirementsText($requirementsText);
		
		// Build modern elegant HTML
		$html = '<div id="' . $describedById . '" class="file-requirements-info">';
		
		$html .= '<div class="box">';
		
		// Icon
		$html .= '<div class="icon">';
		$html .= '<svg width="20" height="20" viewBox="0 0 20 20" fill="none" xmlns="http://www.w3.org/2000/svg" style="display: block;">';
		$html .= '<path d="M10 0C4.48 0 0 4.48 0 10C0 15.52 4.48 20 10 20C15.52 20 20 15.52 20 10C20 4.48 15.52 0 10 0ZM11 15H9V9H11V15ZM11 7H9V5H11V7Z" fill="green" opacity="0.95"/>';
		$html .= '</svg>';
		$html .= '</div>';
		
		// Content
		$html .= '<div class="content-box">';
		$html .= '<div class="title">File Requirements</div>';
		
		if (!empty($requirements['types'])) {
			$html .= '<div class="content">';
			$html .= '<span class="label">Allowed types:</span> ';
			$html .= '<span class="text">' . canvastack_form_escape_html($requirements['types']) . '</span>';
			$html .= '</div>';
		}
		
		if (!empty($requirements['size'])) {
			$html .= '<div class="content">';
			$html .= '<span class="label">Maximum size:</span> ';
			$html .= '<span class="text">' . canvastack_form_escape_html($requirements['size']) . '</span>';
			$html .= '</div>';
		}
		
		$html .= '</div>'; // Close content
		$html .= '</div>'; // Close flex container
		$html .= '</div>'; // Close main container
		
		return $html;
	}
	
	/**
	 * Parse requirements text into structured data
	 * 
	 * @param string $requirementsText Requirements text
	 * 
	 * @return array Parsed requirements
	 */
	private function parseRequirementsText(string $requirementsText): array {
		$parsed = [
			'types' => '',
			'size' => ''
		];
		
		// Split by period
		$parts = explode('. ', $requirementsText);
		
		foreach ($parts as $part) {
			$part = trim($part);
			
			// Check for file types
			if (stripos($part, 'Allowed file types:') !== false) {
				$parsed['types'] = trim(str_ireplace('Allowed file types:', '', $part));
			}
			
			// Check for file size
			if (stripos($part, 'Maximum file size:') !== false) {
				$parsed['size'] = trim(str_ireplace('Maximum file size:', '', $part));
			}
		}
		
		return $parsed;
	}
