	/**
	 * Create Input File
	 *
	 * @param string $name Field name
	 * @param array $attributes HTML attributes
	 * @param boolean $label Whether to show label
	 */
	public function file(string $name, array $attributes = [], bool $label = true): void {
		$this->setParams(__FUNCTION__, $name, null, $attributes, $label);
		$this->inputDraw(__FUNCTION__, $name);
	}
	
	/**
	 * Draw Input File
	 * 
	 * Renders the file input HTML with preview support and ARIA attributes.
	 * All user-facing values are escaped for XSS protection.
	 * 
	 * @param string $name Field name
	 * @param array $attributes HTML attributes
	 * 
	 * @return string Safe HTML file input element
	 * 
	 * @security All output values are escaped
	 * @security File values are validated before display
	 */
	private function inputFile(string $name, array $attributes): string {
		$inputFile = Form::file($name, false);
		$fileValue = $this->getValidatedFileValue($attributes);
		$nameEscaped = canvastack_form_escape_html($name);

		// Get ARIA attributes for accessibility
		$ariaAttributes = $this->getFileAriaAttributes($name, $attributes);

		if ($this->hasImagePreview($attributes)) {
			return $this->renderImagePreviewInput($nameEscaped, $inputFile, $fileValue, $ariaAttributes);
		}

		return $this->renderStandardFileInput($nameEscaped, $inputFile, $fileValue, $ariaAttributes);
	}

	/**
	 * Get validated file value from attributes
	 * 
	 * @param array $attributes HTML attributes
	 * 
	 * @return string Escaped and validated file value
	 */
	private function getValidatedFileValue(array $attributes): string {
		if (empty($attributes['value'])) {
			return '';
		}

		$fileValueRaw = $attributes['value'];

		// Validate that the file path is safe
		if (!$this->validateFilePath($fileValueRaw)) {
			error_log('SECURITY WARNING: Invalid file path in attributes: ' . $fileValueRaw);
			return '';
		}

		return canvastack_form_escape_html($fileValueRaw);
	}

	/**
	 * Check if image preview is enabled
	 * 
	 * @param array $attributes HTML attributes
	 * 
	 * @return bool True if image preview enabled
	 */
	private function hasImagePreview(array $attributes): bool {
		return in_array('imagepreview', $attributes);
	}

	/**
	 * Render file input with image preview
	 * 
	 * @param string $nameEscaped Escaped field name
	 * @param string $inputFile File input HTML
	 * @param string $fileValue Escaped file value
	 * @param array $ariaAttributes ARIA attributes for accessibility
	 * 
	 * @return string Safe HTML
	 */
	private function renderImagePreviewInput(string $nameEscaped, string $inputFile, string $fileValue, array $ariaAttributes): string {
		$previewImage = $fileValue ? "<img src=\"{$fileValue}\" alt=\"Preview\" />" : '';
		
		// Build ARIA attributes string
		$ariaAttrsString = $this->buildAriaAttributesString($ariaAttributes);
		
		// Get requirements description if present
		$requirementsHtml = $this->renderRequirementsDescription($nameEscaped, $ariaAttributes);

		$html  = '<div class="fileinput fileinput-new" data-provides="fileinput">';
		$html .= '<div id="' . $nameEscaped . '-fileinput-preview" class="fileinput-preview thumbnail" data-trigger="fileinput" style="width: 198px; height: 150px;" aria-label="File preview">' . $previewImage . '</div>';
		$html .= '<div>';
		$html .= "<span class=\"btn btn-primary btn-file\"><span class=\"fileinput-new\">Select Image</span><span class=\"fileinput-exists\">Change</span>{$inputFile}</span>";
		$html .= '<a href="#" class="btn btn-danger fileinput-exists" data-dismiss="fileinput" aria-label="Remove file">Remove</a>';
		$html .= '</div>';
		$html .= $requirementsHtml; // Requirements di dalam fileinput
		$html .= '</div>';

		return SafeHtml::mark("<div class=\"input-group col-sm-9\" {$ariaAttrsString}>{$html}</div>");
	}

	/**
	 * Render standard file input
	 * 
	 * @param string $nameEscaped Escaped field name
	 * @param string $inputFile File input HTML
	 * @param string $fileValue Escaped file value
	 * @param array $ariaAttributes ARIA attributes for accessibility
	 * 
	 * @return string Safe HTML
	 */
	private function renderStandardFileInput(string $nameEscaped, string $inputFile, string $fileValue, array $ariaAttributes): string {
		$displayValue = $this->extractFilenameForDisplay($fileValue);
		
		// Build ARIA attributes string
		$ariaAttrsString = $this->buildAriaAttributesString($ariaAttributes);
		
		// Get requirements description if present
		$requirementsHtml = $this->renderRequirementsDescription($nameEscaped, $ariaAttributes);

		$html  = '<div class="fileinput fileinput-new input-group" data-provides="fileinput">';
		$html .= '<div class="form-control" data-trigger="fileinput"><i class="glyphicon glyphicon-file fileinput-exists" aria-hidden="true"></i> <span class="fileinput-filename">' . $displayValue . '</span></div>';
		$html .= "<span class=\"input-group-addon btn btn-primary btn-file\"><span class=\"fileinput-new\">Select File</span><span class=\"fileinput-exists\">Change</span>{$inputFile}</span>";
		$html .= '<a href="#" class="input-group-addon btn btn-danger fileinput-exists" data-dismiss="fileinput" aria-label="Remove file">Remove</a>';
		$html .= $requirementsHtml; // Requirements di dalam fileinput
		$html .= '</div>';

		return SafeHtml::mark("<div class=\"input-group col-sm-9\" {$ariaAttrsString}>{$html}</div>");
	}

	/**
	 * Extract filename for display from file path
	 * 
	 * @param string $fileValue File path
	 * 
	 * @return string Escaped filename
	 */
	private function extractFilenameForDisplay(string $fileValue): string {
		if (empty($fileValue)) {
			return '';
		}

		$fileValueParts = explode('/', $fileValue);
		$fileValueLast = $fileValueParts[array_key_last($fileValueParts)];
		$fileValueName = explode('_', $fileValueLast);
		$displayName = $fileValueName[1] ?? $fileValueLast;

		return canvastack_form_escape_html($displayName);
	}
	
	
	/**
	 * Validate file extension against whitelist
	 * 
	 * @param string $filename Filename to validate
	 * @param array|null $customAllowedExtensions Custom allowed extensions (null = use default)
	 * 
	 * @return bool True if valid
	 * 
	 * @throws \InvalidArgumentException If extension not allowed
	 * 
	 * @security CRITICAL - Prevents malicious file uploads
	 */
	private function validateFileExtension(string $filename, ?array $customAllowedExtensions = null): bool {
		$allowedExtensions = $customAllowedExtensions ?? $this->allowedFileExtensions;
		
		// Get file extension
		$extension = strtolower(pathinfo($filename, PATHINFO_EXTENSION));
		
		if (empty($extension)) {
			error_log('SECURITY WARNING: File upload attempted without extension: ' . $filename);
			throw new \InvalidArgumentException('File must have a valid extension');
		}
		
		// Check against whitelist
		if (!in_array($extension, array_map('strtolower', $allowedExtensions))) {
			error_log('SECURITY WARNING: File upload attempted with disallowed extension: ' . $extension);
			throw new \InvalidArgumentException('File type not allowed. Allowed types: ' . implode(', ', $allowedExtensions));
		}
		
		// Check for double extensions (e.g., file.php.jpg)
		$filenameParts = explode('.', $filename);
		if (count($filenameParts) > 2) {
			// Check if any part before the last extension is a dangerous extension
			$dangerousExtensions = ['php', 'phtml', 'php3', 'php4', 'php5', 'phps', 'pht', 'phar', 'exe', 'sh', 'bat', 'cmd', 'com', 'js', 'jar'];
			for ($i = 0; $i < count($filenameParts) - 1; $i++) {
				if (in_array(strtolower($filenameParts[$i]), $dangerousExtensions)) {
					error_log('SECURITY WARNING: Double extension attack detected: ' . $filename);
					throw new \InvalidArgumentException('File contains dangerous double extension');
				}
			}
		}
		
		return true;
	}
	
	/**
	 * Validate MIME type from actual file content
	 * 
	 * @param object $file Uploaded file object
	 * @param array|null $customAllowedMimes Custom allowed MIME types (null = use default)
	 * 
	 * @return bool True if valid
	 * 
	 * @throws \InvalidArgumentException If MIME type not allowed
	 * 
	 * @security CRITICAL - Validates actual file content, not just extension
	 */
	private function validateMimeType(object $file, ?array $customAllowedMimes = null): bool {
		$allowedMimes = $customAllowedMimes ?? $this->allowedMimeTypes;
		
		$mimeType = $file->getMimeType();
		
		if (!in_array($mimeType, $allowedMimes)) {
			error_log('SECURITY WARNING: File upload attempted with disallowed MIME type: ' . $mimeType);
			throw new \InvalidArgumentException('File type not allowed. MIME type: ' . $mimeType);
		}
		
		return true;
	}
	
	/**
	 * Validate file size
	 * 
	 * @param object $file Uploaded file object
	 * @param int|null $customMaxSize Custom max size in bytes (null = use default)
	 * 
	 * @return bool True if valid
	 * 
	 * @throws \InvalidArgumentException If file too large
	 */
	private function validateFileSize(object $file, ?int $customMaxSize = null): bool {
		$maxSize = $customMaxSize ?? $this->maxFileSize;
		$fileSize = $file->getSize();
		
		if ($fileSize > $maxSize) {
			$maxSizeMB = round($maxSize / 1048576, 2);
			$fileSizeMB = round($fileSize / 1048576, 2);
			error_log('SECURITY WARNING: File upload attempted exceeding size limit: ' . $fileSizeMB . 'MB (max: ' . $maxSizeMB . 'MB)');
			throw new \InvalidArgumentException('File too large. Maximum size: ' . $maxSizeMB . 'MB');
		}
		
		return true;
	}
	
	/**
	 * Validate upload path for directory traversal
	 * 
	 * @param string $path Path to validate
	 * @param string $baseDir Base directory that must contain the path
	 * 
	 * @return bool True if safe
	 * 
	 * @throws \InvalidArgumentException If path traversal detected
	 * 
	 * @security CRITICAL - Prevents directory traversal attacks
	 */
	private function validateUploadPath(string $path, string $baseDir): bool {
		// Normalize paths
		$realPath = realpath($path) ?: $path;
		$realBase = realpath($baseDir) ?: $baseDir;
		
		// Convert to forward slashes for consistency
		$realPath = str_replace('\\', '/', $realPath);
		$realBase = str_replace('\\', '/', $realBase);
		
		// Check for directory traversal patterns
		if (str_contains($path, '..') || str_contains($path, './') || str_contains($path, '.\\')) {
			error_log('SECURITY WARNING: Directory traversal attempt detected: ' . $path);
			throw new \InvalidArgumentException('Invalid upload path: directory traversal detected');
		}
		
		// Ensure path is within base directory
		if (!str_starts_with($realPath, $realBase)) {
			error_log('SECURITY WARNING: Upload path outside base directory: ' . $path);
			throw new \InvalidArgumentException('Invalid upload path: must be within allowed directory');
		}
		
		return true;
	}
	
	/**
	 * Validate file path for display (prevents XSS in file paths)
	 * 
	 * @param string $filePath File path to validate
	 * 
	 * @return bool True if safe
	 */
	private function validateFilePath(string $filePath): bool {
		// Check for dangerous characters
		$dangerousChars = ['<', '>', '"', "'", '`', ';', '|', '&', '$', '(', ')', '{', '}', '[', ']'];
		foreach ($dangerousChars as $char) {
			if (str_contains($filePath, $char)) {
				return false;
			}
		}
		
		// Check for script tags or javascript
		if (stripos($filePath, 'script') !== false || stripos($filePath, 'javascript') !== false) {
			return false;
		}
		
		return true;
	}
	
	/**
	 * Generate random secure filename
	 * 
	 * @param string $originalFilename Original filename
	 * 
	 * @return string Random filename with original extension
	 * 
	 * @security Prevents overwrite attacks and filename-based exploits
	 */
	private function generateSecureFilename(string $originalFilename): string {
		$extension = strtolower(pathinfo($originalFilename, PATHINFO_EXTENSION));
		
		// Generate random filename using secure random bytes
		$randomName = bin2hex(random_bytes(16));
		$timestamp = time();
		
		return "{$timestamp}_{$randomName}.{$extension}";
	}
	
	/**
	 * Set secure file permissions
	 * 
	 * @param string $filePath Full path to file
	 * 
	 * @return bool True if successful
	 * 
	 * @security Sets file to 0644 (read-only for group/others)
	 */
	private function setSecureFilePermissions(string $filePath): bool {
		try {
			chmod($filePath, 0644);
			return true;
		} catch (\Exception $e) {
			error_log('WARNING: Failed to set file permissions for: ' . $filePath . ' - ' . $e->getMessage());
			return false;
		}
	}
	
	/**
	 * Clean up uploaded file on error
	 * 
	 * @param string $filePath Full path to file to delete
	 * 
	 * @return void
	 */
	private function cleanupFile(string $filePath): void {
		if (file_exists($filePath)) {
			try {
				unlink($filePath);
				error_log('INFO: Cleaned up file after validation failure: ' . $filePath);
			} catch (\Exception $e) {
				error_log('ERROR: Failed to cleanup file: ' . $filePath . ' - ' . $e->getMessage());
			}
		}
	}
	
