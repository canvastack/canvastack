	/**
	 * Create Image Thumbnail with Security Validation
	 * 
	 * @param object $request Request object
	 * @param string $inputname Input field name
	 * @param array $dataInfo Thumbnail configuration
	 * @param string $upload_path Upload directory path
	 * 
	 * @return boolean True if successful, false otherwise
	 * 
	 * @throws \InvalidArgumentException If image validation fails
	 * 
	 * @security Validates image file before processing
	 * @security Validates thumbnail path for traversal
	 * @security Sets secure permissions on thumbnail
	 */
	private function createThumbImage(object $request, string $inputname, array $dataInfo, string $upload_path): bool {
		if (empty($dataInfo['thumb_name'])) {
			return false;
		}

		$thumbPath = $this->prepareThumbnailPath($upload_path);
		$thumbname = 'tnail_' . $this->fileNameInfo;
		
