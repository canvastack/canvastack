	/**
	 * Validate and upload file
	 * 
	 * @param object $request Request object
	 * @param string $inputname Input field name
	 * @param array $elements File configuration
	 * @param string $filePath Upload path
	 * @param string $upload_path Base upload path
	 * 
	 * @return void
	 * 
	 * @throws \InvalidArgumentException If validation fails
	 */
	private function validateAndUploadFile(object $request, string $inputname, array $elements, string $filePath, string $upload_path): void {
		if (!empty($elements['file_validation'])) {
			$this->validationFile($request, $inputname, $elements['file_validation']);
		}
		
		canvastack_make_dir($filePath, 0777, true, true);
		
		if (is_array($request->file($inputname))) {
			// TODO: Handle multiple file uploads
			error_log('INFO: Multiple file upload not yet implemented for: ' . $inputname);
			return;
		}
		
		$this->processSingleFile($request, $inputname, $elements, $filePath, $upload_path);
	}

	/**
	 * Process single file upload with security validation
	 * 
	 * @param object $request Request object
	 * @param string $inputname Input field name
	 * @param array $elements File configuration
	 * @param string $filePath Upload path
	 * @param string $upload_path Base upload path
	 * 
	 * @return void
	 * 
	 * @throws \InvalidArgumentException If validation fails
	 */
	private function processSingleFile(object $request, string $inputname, array $elements, string $filePath, string $upload_path): void {
		$file = $request->file($inputname);
		
		// Security Layer 1: Validate file extension
		$originalFilename = $file->getClientOriginalName();
		$customExtensions = $elements['allowed_extensions'] ?? null;
		$this->validateFileExtension($originalFilename, $customExtensions);
		
		// Security Layer 2: Validate MIME type from actual content
		$customMimes = $elements['allowed_mimes'] ?? null;
		$this->validateMimeType($file, $customMimes);
		
		// Security Layer 3: Validate file size
		$customMaxSize = $elements['max_file_size'] ?? null;
		$this->validateFileSize($file, $customMaxSize);
		
		// Security Layer 4: Generate secure random filename
		$filename = $this->generateSecureFilename($originalFilename);
		$this->fileNameInfo = $filename;
		
		// Log successful validation
		error_log('INFO: File upload validated successfully: ' . $inputname . ' -> ' . $filename);
		
		$fileType = $this->getFileType($request, $inputname);
		$elements = $this->ensureFileElements($elements, $fileType, $originalFilename);
		
		// Security: Create thumbnail for images with validation
		if ($fileType === 'image') {
			$this->createThumbnailIfNeeded($request, $inputname, $elements, $upload_path);
		}
		
		// Move file to final location
		$file->move($filePath, $filename);
		
		// Security Layer 5: Set secure file permissions (0644)
		$fullFilePath = $filePath . '/' . $filename;
		$this->setSecureFilePermissions($fullFilePath);
		
		// Store file path for retrieval
		$this->getFileUploads[$inputname]['file'] = $this->setAssetPath($filePath, $filename);
		
		// Log successful upload
		error_log('INFO: File uploaded successfully: ' . $inputname . ' -> ' . $fullFilePath);
	}

