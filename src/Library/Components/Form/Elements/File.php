	/**
	 * Verify asset path is HTTP accessible
	 *
	 * This method verifies that the generated asset path is properly formatted
	 * for HTTP access and points to a valid location within the public directory.
	 *
	 * @param string $assetPath Asset path to verify
	 *
	 * @return bool True if path is valid and accessible, false otherwise
	 *
	 * @security Validates path is within allowed public directories
	 */
	public function verifyAssetPathAccessible(string $assetPath): bool {
		// Remove leading slash if present
		$assetPath = ltrim($assetPath, '/');

		// Check if path starts with expected base folder
		$baseIndexFolder = canvastack_config('index_folder');
		if ($baseIndexFolder && !str_starts_with($assetPath, $baseIndexFolder)) {
			return false;
		}

		// Verify path doesn't contain dangerous patterns
		if (str_contains($assetPath, '..') || str_contains($assetPath, '\\')) {
			return false;
		}

		// Construct full file path
		$fullPath = public_path($assetPath);

		// Verify file exists and is readable
		if (!file_exists($fullPath) || !is_readable($fullPath)) {
			return false;
		}

		// Verify path is within public directory
		$publicPath = public_path();
		$realFullPath = realpath($fullPath);
		$realPublicPath = realpath($publicPath);

		if ($realFullPath === false || $realPublicPath === false) {
			return false;
		}

		return str_starts_with($realFullPath, $realPublicPath);
	}

	/**
	 * Verify thumbnail path is correct and accessible
	 *
	 * This method verifies that the generated thumbnail path is properly formatted,
	 * points to the correct location, and is HTTP accessible.
	 *
	 * @param string $thumbnailPath Thumbnail path to verify
	 *
	 * @return bool True if thumbnail path is valid and accessible, false otherwise
	 *
	 * @security Validates thumbnail path is within allowed directories
	 */
	public function verifyThumbnailPath(string $thumbnailPath): bool {
		// Use the same verification logic as asset paths
		if (!$this->verifyAssetPathAccessible($thumbnailPath)) {
			return false;
		}

		// Additional check: verify path contains thumbnail folder indicator
		$thumbFolder = $this->thumbFolder ?? 'thumb';
		if (!str_contains($thumbnailPath, $thumbFolder)) {
			error_log('WARNING: Thumbnail path does not contain expected folder: ' . $thumbnailPath);
			return false;
		}

		// Verify thumbnail filename starts with expected prefix
		$filename = basename($thumbnailPath);
		if (!str_starts_with($filename, 'tnail_')) {
			error_log('WARNING: Thumbnail filename does not have expected prefix: ' . $filename);
			return false;
		}

		return true;
	}

