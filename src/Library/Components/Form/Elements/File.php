	/**
	 * Process single file upload
	 * 
	 * @param object $request Request object
	 * @param string $inputname Input field name
	 * @param array $fileInfo File configuration
	 * @param string $filePath Upload path
	 * @param string $upload_path Base upload path
	 * 
	 * @return void
	 */
	private function processFileUpload(object $request, string $inputname, array $fileInfo, string $filePath, string $upload_path): void {
		$elements = $this->getFileElements($fileInfo, $inputname);
		
		if (!$request->hasfile($inputname)) {
			return;
		}

		// Security: Validate file before processing
		try {
			$this->validateAndUploadFile($request, $inputname, $elements, $filePath, $upload_path);
		} catch (\InvalidArgumentException $e) {
			// Security: Clean up any partially uploaded files
			if (isset($fullFilePath)) {
				$this->cleanupFile($fullFilePath);
			}
			
			// Log security event
			error_log('SECURITY ERROR: File upload validation failed for ' . $inputname . ': ' . $e->getMessage());
			
			// Re-throw exception for controller to handle
			throw $e;
			
		} catch (\Exception $e) {
			// Clean up on any error
			if (isset($fullFilePath)) {
				$this->cleanupFile($fullFilePath);
			}
			
			error_log('ERROR: File upload failed for ' . $inputname . ': ' . $e->getMessage());
			throw $e;
		}
	}

