	/**
	 * Test file upload round-trip property
	 *
	 * This method verifies the complete upload → retrieve → display flow:
	 * 1. File is uploaded and saved
	 * 2. Asset path is generated and stored
	 * 3. Asset path can be retrieved
	 * 4. Asset path is HTTP accessible
	 * 5. Thumbnail path (if applicable) is also correct and accessible
	 *
	 * @param string $inputname Input field name to test
	 *
	 * @return array Test results with status and details
	 *
	 * @example
	 * $results = $form->testFileUploadRoundTrip('avatar');
	 * // Returns: [
	 * //   'success' => true,
	 * //   'file_path' => 'assets/uploads/2024/01/15/abc123.jpg',
	 * //   'file_accessible' => true,
	 * //   'thumbnail_path' => 'assets/uploads/2024/01/15/thumb/tnail_abc123.jpg',
	 * //   'thumbnail_accessible' => true,
	 * //   'errors' => []
	 * // ]
	 */
	public function testFileUploadRoundTrip(string $inputname): array {
		$results = [
			'success' => false,
			'file_path' => null,
			'file_accessible' => false,
			'thumbnail_path' => null,
			'thumbnail_accessible' => false,
			'errors' => []
		];

		// Step 1: Retrieve uploaded file paths
		$uploadedPaths = $this->getUploadedFilePaths($inputname);

		if ($uploadedPaths === null) {
			$results['errors'][] = "No uploaded file found for input: {$inputname}";
			return $results;
		}

		// Step 2: Verify main file path exists
		if (!isset($uploadedPaths['file'])) {
			$results['errors'][] = "File path not set for input: {$inputname}";
			return $results;
		}

		$filePath = $uploadedPaths['file'];
		$results['file_path'] = $filePath;

		// Step 3: Verify file path is HTTP accessible
		try {
			$results['file_accessible'] = $this->verifyAssetPathAccessible($filePath);

			if (!$results['file_accessible']) {
				$results['errors'][] = "File path is not HTTP accessible: {$filePath}";
			}
		} catch (\Exception $e) {
			$results['errors'][] = "Error verifying file accessibility: " . $e->getMessage();
		}

		// Step 4: Verify thumbnail path if present
		if (isset($uploadedPaths['thumbnail'])) {
			$thumbnailPath = $uploadedPaths['thumbnail'];
			$results['thumbnail_path'] = $thumbnailPath;

			try {
				$results['thumbnail_accessible'] = $this->verifyThumbnailPath($thumbnailPath);

				if (!$results['thumbnail_accessible']) {
					$results['errors'][] = "Thumbnail path is not accessible: {$thumbnailPath}";
				}
			} catch (\Exception $e) {
				$results['errors'][] = "Error verifying thumbnail accessibility: " . $e->getMessage();
			}
		}

		// Step 5: Determine overall success
		$results['success'] = $results['file_accessible'] &&
		                      empty($results['errors']) &&
		                      (!isset($uploadedPaths['thumbnail']) || $results['thumbnail_accessible']);

		return $results;
	}

